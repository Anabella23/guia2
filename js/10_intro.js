let mes = parseInt(prompt("Ingrese el número del mes"));

switch (mes) {
    case 1:
        console.log("ENERO");
        break;
    case 2:
        console.log("FEBRERO");
        break;
    case 3:
        console.log("MARZO");
        break;
    case 4:
        console.log("ABRIL");
        break;
    case 5:
        console.log("MAYO");
        break;
    case 6:
        console.log( "JUNIO");
        break;
    case 7:
        console.log("JULIO");
        break;
    case 8:
        console.log("AGOSTO");
        break;
    case 9:
        console.log("SEPTIEMBRE");
        break;
    case 10:
        console.log("OCTUBRE");
        break;
    case 11:
        console.log("NOVIEMBRE");
        break;
    case 12:
        console.log("DICIEMBRE");
        break;

    default: console.log("MES INVÁLIDO");
        
}
